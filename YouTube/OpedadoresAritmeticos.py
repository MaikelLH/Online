#Operadores Aritméticos

#Suma
num1 = 10
num2 = 5
Resultado = num1 + num2
print("El Resultado de la Suma es",Resultado)

#Resta
Resultado = num1 - num2
print("El Resultado de la Resta es",Resultado)

#Multiplicación
Resultado = num1 * num2
print("El Resultado de la Multiplicación es",Resultado)

#División
Resultado = num1 / num2
print("El Resultado de la División Simple es",Resultado)

#División Entera Redondeado a la Baja (Sin desimales)
Resultado = num1 // num2
print("El Resultado de la División Entera es",Resultado)

#Modulo es el restante (reciduo) de la división
Resultado = num1 % num2
print("El Resultado del Modulo es",Resultado)

#Exponenciación
Resultado = num1 ** num2
print("El Resultado de la Exponencia es",Resultado)

