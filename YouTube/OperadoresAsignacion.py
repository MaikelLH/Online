#Operadores de Asignación

'''Sirve para acortar la programación, primero debemos definir el valor de una variable'''

a = 0
print("Valor Inicial",a)
a += 5 # Suma en asignación
print("+5: Suma en Asignación el resultado es",a)

a -= 2 # Resta en asignación
print("-2: Resta en Asignación el resultado es",a)

a *= 3 # Multipliación en asignación
print("*3: Multiplicación en Asignación el resultado es",a)

a /= 3 # División en asignación
print("/3: División en Asignación el resultado es",a)

a **= 2 # Potencia en asignación
print("**2: Potencia en Asignación el resultado es",a)

a %= 2 # Modulo en asignación
print("%2: Modulo en Asignación el resultado es",a)